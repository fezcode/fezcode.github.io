function theme(id, label, def) {
  return {
    id,
    label,
    grain: def.grain ?? 0.05,
    paper: def.paper || BASE_UI.bg,
    sprite: { ...BASE_SPRITE, ...(def.sprite || {}) },
    terrain: { ...BASE_TERRAIN, ...(def.terrain || {}) },
    ui: { ...BASE_UI, ...(def.ui || {}) },
  };
}

